# criando a tupla
minha_tupla = (1, 2, 2, 3, 4, 23)
print("minha_tupla ->", minha_tupla)

print("minha_tupla[3] ->", minha_tupla[3])
print("minha_tupla[-1] ->", minha_tupla[-1])

print("quantos 23 tem dentro da tupla ->", minha_tupla.count(23))
print("em qual posição está o elemento 4 ->", minha_tupla.index(4))
print("tamanho da tupla ->", len(minha_tupla))